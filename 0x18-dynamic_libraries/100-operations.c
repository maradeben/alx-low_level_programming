#include "operations.h"

/**
 * add - add two numbers together
 * @a: first num
 * @b: second num
 * Return: the sum of the numbers
 */
int add(int a, int b)
{
	return (a + b);
}

/**
 * sub - subract b from a
 * @a: first num
 * @b: second num
 * Return: the difference numbers
 */
int sub(int a, int b)
{
	return (a - b);
}

/**
 * mul - multiply two numbers
 * @a: first num
 * @b: second num
 * Return: the product of the numbers
 */
int mul(int a, int b)
{
	return (a * b);
}

/**
 * div - divide a by b
 * @a: first num
 * @b: second num
 * Return: the result of division of the numbers
 */
int div(int a, int b)
{
	return (a / b);
}

/**
 * mod - get modulus of numbers
 * @a: first num
 * @b: second num
 * Return: the result of modular operation
 */
int mod(int a, int b)
{
	return (a % b);
}
